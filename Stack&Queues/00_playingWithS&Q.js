//Stack Implementation in Js

let stack=[];

stack.push(1)
stack.push(2)
stack.push(3)
stack.push(4)
console.log(stack)

stack.pop()
stack.pop()
console.log(stack)

//Peek/top
let topS=stack[stack.length-1]
console.log(topS)

